/**
 * Operadores logicos
 * && -> AND -> E
 * || -> OR -> OU
 * !  -> NOT -> NAO
 */

console.log(true && true && true);
console.log(true && true && false);
console.log(false && false && false);

console.log(true || true || true);
console.log(false || true || false);
console.log(false || false || false);

console.log(!false);
console.log(!true);